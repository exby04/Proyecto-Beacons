    private static final String MI_BEACON_NOMBRE = "GTI"; // nombre esperado

    private void mostrarInformacionDispositivoBTLE(ScanResult resultado) {
        BluetoothDevice bluetoothDevice = resultado.getDevice();
        byte[] bytes = resultado.getScanRecord().getBytes();

        // üîç FILTRO: solo si es nuestro beacon por nombre
        if (ActivityCompat.checkSelfPermission(this, Manifest.permission.BLUETOOTH_CONNECT) != PackageManager.PERMISSION_GRANTED) {
            // TODO: Consider calling
            //    ActivityCompat#requestPermissions
            // here to request the missing permissions, and then overriding
            //   public void onRequestPermissionsResult(int requestCode, String[] permissions,
            //                                          int[] grantResults)
            // to handle the case where the user grants the permission. See the documentation
            // for ActivityCompat#requestPermissions for more details.
            return;
        }
        String nombre = bluetoothDevice.getName();
        if (nombre == null || !MI_BEACON_NOMBRE.equals(nombre)) {
            return; // ignoramos otros beacons
        }

        int rssi = resultado.getRssi();


        TramaIBeacon tib = new TramaIBeacon(bytes);
        BeaconMedicion medicion = new BeaconMedicion(
                tib,
                bluetoothDevice.getAddress(),
                resultado.getRssi()
        );
        Log.d(ETIQUETA_LOG, "Medici√≥n interpretada: " + medicion.descripcion());

        // üëâ Aqu√≠ ya sabes que es tuyo
        Log.d(ETIQUETA_LOG, "****************************************************");
        Log.d(ETIQUETA_LOG, "****** BEACON PROPIO DETECTADO *********************");
        Log.d(ETIQUETA_LOG, "****************************************************");
        if (ActivityCompat.checkSelfPermission(this, Manifest.permission.BLUETOOTH_CONNECT) != PackageManager.PERMISSION_GRANTED) {
            return;
        }
        Log.d(ETIQUETA_LOG, "nombre = " + nombre);
        Log.d(ETIQUETA_LOG, "direcci√≥n = " + bluetoothDevice.getAddress());
        Log.d(ETIQUETA_LOG, "rssi = " + rssi);
        Log.d(ETIQUETA_LOG, "Medici√≥n interpretada: " + medicion.descripcion());
        Log.d(ETIQUETA_LOG, "****************************************************");
    }


